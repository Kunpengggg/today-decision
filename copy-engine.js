function copyDecisionKey(decision) {
  return decision === "适合做" ? "go" : decision === "建议缓做" ? "wait" : "stop";
}

function getCopySet(contextKey, decision, seed = 0) {
  const group = decisionCopyLibrary[contextKey] || decisionCopyLibrary["普通可逆"];
  const item = group[copyDecisionKey(decision)] || group.wait;
  const title = item.titles[Math.abs(seed) % item.titles.length];
  return { ...item, title };
}

function buildHumanAdvice({ question, decision, score, confidence, decisionContext, timeContext, followup, seed }) {
  const copy = getCopySet(decisionContext.key, decision, seed);
  const followupLine = followup.score >= 6
    ? `你补充的信息是加分项，说明这件事不是纯冲动。`
    : followup.score <= -4
      ? `你补充的信息里有阻力，所以别急着把动作做大。`
      : `现在信息还不算满，先别把它想成最终决定。`;
  return {
    title: copy.title,
    lead: `${copy.lead}${followupLine}`,
    action: copy.action,
    why: `参考${timeContext.name}窗口和你的输入，倾向值 ${score}，可信度${confidence}。`
  };
}
